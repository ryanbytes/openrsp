static float interpolate(float a, float b, double fraction)
{
    return (float)((double)a + ((double)b - (double)a) * fraction);
}

int openrsp_rspduo_gain_values(double rf_hz, unsigned int lna_state,
                               int gain_reduction_db,
                               int minimum_gain_reduction_db,
                               int high_impedance_input, float *current,
                               float *maximum, float *minimum)
{
    if (!isfinite(rf_hz) || rf_hz < gain_knots[0].rf_hz ||
        rf_hz > gain_knots[sizeof(gain_knots) / sizeof(gain_knots[0]) - 1u].rf_hz ||
        (minimum_gain_reduction_db != 0 && minimum_gain_reduction_db != 20) ||
        gain_reduction_db < minimum_gain_reduction_db ||
        gain_reduction_db > 59 || current == NULL ||
        maximum == NULL || minimum == NULL)
        return -1;

    if (high_impedance_input && rf_hz >= 30000000.0) {
        if (lna_state >= 5u) return -1;
        *current = 0.0f;
        *maximum = 0.0f;
        *minimum = 0.0f;
        return 0;
    }

    const rspduo_gain_knot *knots = high_impedance_input ?
        high_impedance_gain_knots : gain_knots;
    size_t count = high_impedance_input ?
        sizeof(high_impedance_gain_knots) /
            sizeof(high_impedance_gain_knots[0]) :
        sizeof(gain_knots) / sizeof(gain_knots[0]);
    double lookup_hz = high_impedance_input || rf_hz < 1000.0 ? rf_hz :
        floor((rf_hz + 500.0) / 1000.0) * 1000.0;
    const rspduo_gain_knot *low = &knots[0];
    const rspduo_gain_knot *high = low;
    for (size_t index = 0u; index < count; ++index) {
        if (lookup_hz == knots[index].rf_hz) {
            low = high = &knots[index];
            break;
        }
        if (lookup_hz < knots[index].rf_hz) {
            high = &knots[index];
            low = index == 0u ? high : &knots[index - 1u];
            break;
        }
        low = high = &knots[index];
    }
    if (lna_state >= low->state_count || lna_state >= high->state_count)
        return -1;
    double fraction = high == low ? 0.0 :
        (lookup_hz - low->rf_hz) / (high->rf_hz - low->rf_hz);
    float calibrated_max = interpolate(low->maximum, high->maximum, fraction);
    float calibrated_min = interpolate(low->minimum, high->minimum, fraction);
    float lna_reduction = interpolate(low->lna_reduction[lna_state],
                                      high->lna_reduction[lna_state], fraction);
    *maximum = calibrated_max + (float)(20 - minimum_gain_reduction_db);
    *minimum = calibrated_min;
    *current = calibrated_max - (float)(gain_reduction_db - 20) - lna_reduction;
    return 0;
}
